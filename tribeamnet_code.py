class AntiRectifier(nn.Module):
    """Improved AntiRectifier with better normalization"""
    def __init__(self, eps: float = 1e-8):
        super().__init__()
        self.eps = eps

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        # Center the data
        x = x - torch.mean(x, dim=1, keepdim=True)

        # Normalize with epsilon for stability
        x = F.normalize(x, p=2, dim=1, eps=self.eps)

        # Concatenate positive and negative parts
        return torch.cat([F.relu(x), F.relu(-x)], dim=1)

class DoubleConv(nn.Module):
    def __init__(self, in_channels: int, out_channels: int,
                 mid_channels: Optional[int] = None):
        super().__init__()
        mid_channels = mid_channels or out_channels

        self.conv1 = nn.Conv2d(in_channels, mid_channels, kernel_size=3, padding=1, bias=False)
        self.bn1 = nn.BatchNorm2d(mid_channels)
        self.act1 = AntiRectifier()

        self.conv2 = nn.Conv2d(2 * mid_channels, out_channels, kernel_size=3, padding=1, bias=False)
        self.bn2 = nn.BatchNorm2d(out_channels)
        self.act2 = nn.ReLU(inplace=True)

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        x = self.conv1(x)
        x = self.bn1(x)
        x = self.act1(x)

        x = self.conv2(x)
        x = self.bn2(x)
        x = self.act2(x)

        return x

class Down(nn.Module):
    """Downsampling block"""
    def __init__(self, in_channels: int, out_channels: int):
        super().__init__()
        self.block = nn.Sequential(
            nn.MaxPool2d(2),
            DoubleConv(in_channels, out_channels)
        )

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        return self.block(x)

class Up(nn.Module):
    """Upsampling block with improved concatenation"""
    def __init__(self, in_channels: int, out_channels: int,
                 bilinear: bool = True):
        super().__init__()
        if bilinear:
            self.up = nn.Upsample(scale_factor=2, mode='bilinear', align_corners=True)
            self.conv = DoubleConv(in_channels, out_channels, in_channels // 2)
        else:
            self.up = nn.ConvTranspose2d(in_channels, in_channels // 2, kernel_size=2, stride=2)
            self.conv = DoubleConv(in_channels, out_channels)

    def forward(self, x1: torch.Tensor, x2: torch.Tensor) -> torch.Tensor:
        x1 = self.up(x1)
        # Ensure spatial dimensions match
        x1 = F.interpolate(x1, size=x2.shape[2:], mode='bilinear', align_corners=False)
        x = torch.cat([x2, x1], dim=1)
        return self.conv(x)

class BeamformingHead(nn.Module):
    """Improved beamforming head with better weight initialization"""
    def __init__(self, in_channels: int, n_elements: int):
        super().__init__()
        self.conv1 = nn.Conv2d(in_channels, n_elements * 2, kernel_size=3, padding=1, bias=False)
        self.bn1 = nn.BatchNorm2d(n_elements * 2)
        self.conv2 = nn.Conv2d(n_elements * 2, n_elements, kernel_size=1)

        # Initialize weights properly
        self._initialize_weights()

    def _initialize_weights(self):
        for m in self.modules():
            if isinstance(m, nn.Conv2d):
                nn.init.kaiming_normal_(m.weight, mode='fan_out', nonlinearity='relu')
                if m.bias is not None:
                    nn.init.constant_(m.bias, 0)

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        x = F.relu(self.bn1(self.conv1(x)))
        weights = self.conv2(x)
        return F.softmax(weights, dim=1)

class FixedUNetBeamformer(nn.Module):
    """Improved UNet beamformer with better architecture"""
    def __init__(self, n_elements: int = 128, n_tasks: int = 3):
        super().__init__()
        self.n_elements = n_elements

        # Encoder
        self.inc = DoubleConv(n_elements, 64)
        self.down1 = Down(64, 128)
        self.down2 = Down(128, 256)
        self.down3 = Down(256, 512)

        # Decoder
        self.up1 = Up(768, 256, bilinear=True)
        self.up2 = Up(384, 128, bilinear=True)
        self.up3 = Up(192, 64, bilinear=True)

        # Task-specific heads
        self.das_head = BeamformingHead(64, n_elements)
        self.fdmas_head = BeamformingHead(64, n_elements)
        self.capon_head = BeamformingHead(64, n_elements)

        # Initialize weights
        self._initialize_weights()

    def _initialize_weights(self):
        for m in self.modules():
            if isinstance(m, nn.Conv2d):
                nn.init.kaiming_normal_(m.weight, mode='fan_out', nonlinearity='relu')
                if m.bias is not None:
                    nn.init.constant_(m.bias, 0)
            elif isinstance(m, nn.BatchNorm2d):
                nn.init.constant_(m.weight, 1)
                nn.init.constant_(m.bias, 0)

    def forward(self, x: torch.Tensor) -> Dict[str, torch.Tensor]:
        # Store original input for beamforming
        original_input = x

        # Add input validation
        if torch.isnan(x).any() or torch.isinf(x).any():
            print("NaN or Inf detected in input")
            x = torch.nan_to_num(x, nan=0.0, posinf=1.0, neginf=-1.0)

        # Encoder
        x1 = self.inc(x)
        x2 = self.down1(x1)
        x3 = self.down2(x2)
        x4 = self.down3(x3)

        # Decoder
        x = self.up1(x4, x3)
        x = self.up2(x, x2)
        features = self.up3(x, x1)

        # Generate weights
        das_weights = self.das_head(features)
        fdmas_weights = self.fdmas_head(features)
        capon_weights = self.capon_head(features)

        # Beamforming with input validation
        original_input = torch.nan_to_num(original_input, nan=0.0, posinf=1.0, neginf=-1.0)

        das_output = torch.sum(das_weights * original_input, dim=1, keepdim=True)
        fdmas_output = torch.sum(fdmas_weights * original_input, dim=1, keepdim=True)
        capon_output = torch.sum(capon_weights * original_input, dim=1, keepdim=True)

        return {
            'das': das_output,
            'fdmas': fdmas_output,
            'capon': capon_output
        }

class FixedCustomDatasetTriBeamNet(Dataset):
    """Improved dataset class for TriBeamNet with configurable sampling and probe settings"""
    def __init__(self, folder_path: str, patch_rows: int = 128,
                 seed: int = 42, max_files: int = 1000,
                 fs: int = 31250000, probe_geometry: np.ndarray = None):
        super().__init__()
        self.folder_path = Path(folder_path)
        self.file_paths = sorted([f for f in self.folder_path.glob("*.h5")])[:max_files]
        self.rows = patch_rows
        self.fs = fs
        self.probe_geometry = probe_geometry if probe_geometry is not None else np.linspace(-0.019105, 0.019105, 128)
        random.seed(seed)

        if not self.file_paths:
            raise ValueError(f"No HDF5 files found in {folder_path}")

        print(f"Dataset initialized with {len(self.file_paths)} files")

        self.global_mean = 0.0
        self.global_std = 1.0
        self.compute_global_stats()

    def compute_global_stats(self):
        """Compute global statistics for normalization"""
        print("Computing global statistics...")
        all_values = []
        sample_size = min(10, len(self.file_paths))

        for path in self.file_paths[:sample_size]:
            try:
                with h5py.File(path, 'r') as f:
                    raw_rf = f['raw_data'][:][0].T
                    tofc = self.tofc_mapping(raw_rf)
                    tofc = np.nan_to_num(tofc, nan=0.0, posinf=1.0, neginf=-1.0)
                    all_values.append(tofc.flatten())
            except Exception as e:
                print(f"Error processing {path}: {e}")

        if all_values:
            all_values = np.concatenate(all_values)
            self.global_mean = np.median(all_values)
            self.global_std = np.std(all_values)
            if self.global_std == 0:
                self.global_std = 1.0
            print(f"Global stats - Mean: {self.global_mean:.4f}, Std: {self.global_std:.4f}")
        else:
            print("No valid data found for stats computation. Using default values.")

    def __len__(self) -> int:
        return len(self.file_paths)

    def __getitem__(self, idx: int) -> Dict[str, torch.Tensor]:
        path = self.file_paths[idx]
        try:
            with h5py.File(path, 'r') as f:
                raw_rf = f['raw_data'][:][0].T

                gt_bf_datas = []
                for key in ['DAS', 'FDMAS', 'Capon']:
                    try:
                        bf_data = f[f'bf_data_{key}_real'][:].astype(np.float32)
                        scale = f.attrs[f'scale_{key}_real']
                        bf_data *= scale
                        bf_data = np.nan_to_num(bf_data, nan=0.0, posinf=1.0, neginf=-1.0)
                        if bf_data.shape[1] == 4352:
                            bf_data = decimate(bf_data, q=2, axis=1)
                        gt_bf_datas.append(bf_data)
                    except Exception as e:
                        print(f"Missing {key} in {path.name}, filling with zeros. Error: {e}")
                        gt_bf_datas.append(np.zeros((128, 2176), dtype=np.float32))

                tofc = self.tofc_mapping(raw_rf)
                tofc = np.nan_to_num(tofc, nan=0.0, posinf=1.0, neginf=-1.0)

                valid_rows = tofc.shape[0]
                max_start = max(0, valid_rows - self.rows)
                start = np.random.randint(0, max_start + 1) if max_start > 0 else 0
                end = start + self.rows

                input_patch = tofc[start:end, :, :]
                gt_patches = [gt[:, start:end].T for gt in gt_bf_datas]

                if input_patch.shape[0] != self.rows:
                    pad = self.rows - input_patch.shape[0]
                    input_patch = np.pad(input_patch, ((0, pad), (0, 0), (0, 0)), mode='constant')
                    gt_patches = [np.pad(p, ((0, pad), (0, 0)), mode='constant') for p in gt_patches]

                input_patch = self.normalize_input(input_patch)
                gt_patches = [self.normalize_target(p) for p in gt_patches]

                input_tensor = torch.from_numpy(input_patch.transpose(2, 0, 1)).float()
                output_tensor = torch.from_numpy(np.stack(gt_patches, axis=0)).float()

                return {'input': input_tensor, 'output': output_tensor}

        except Exception as e:
            print(f"Error loading file {path}: {e}")
            return {'input': torch.randn(128, self.rows, 128), 'output': torch.randn(3, self.rows, 128)}

    def normalize_input(self, data: np.ndarray) -> np.ndarray:
        return (data - self.global_mean) / (self.global_std + 1e-8)

    def normalize_target(self, data: np.ndarray) -> np.ndarray:
        mean, std = np.mean(data), np.std(data)
        if std == 0: std = 1.0
        return (data - mean) / (std + 1e-8)

    def tofc_mapping(self, rf_data: np.ndarray) -> np.ndarray:
        try:
            c = 1540
            time = np.arange(rf_data.shape[0]) / self.fs
            x = np.linspace(self.probe_geometry[0], self.probe_geometry[-1], rf_data.shape[1])
            z = 0.5 * c * time
            xg, zg = np.meshgrid(x, z)
            x_flat, z_flat = xg.ravel(), zg.ravel()

            delay_map = np.zeros((rf_data.shape[0] * rf_data.shape[1], rf_data.shape[1]))
            for i in range(rf_data.shape[1]):
                rx_delay = np.sqrt((self.probe_geometry[i] - x_flat) ** 2 + z_flat ** 2)
                total_delay = (z_flat + rx_delay) / c
                interp = interp1d(time, rf_data[:, i], kind='cubic', fill_value=0, bounds_error=False)
                delay_map[:, i] = interp(total_delay)

            return delay_map.reshape(rf_data.shape[0], rf_data.shape[1], rf_data.shape[1])
        except Exception as e:
            print(f"TOFC mapping failed: {e}")
            return np.zeros((rf_data.shape[0], rf_data.shape[1], rf_data.shape[1]))

