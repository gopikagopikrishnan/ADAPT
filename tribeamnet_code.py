def compute_global_stats(tofc_data):
    """Compute global statistics for consistent normalization"""
    global_mean = np.mean(tofc_data)
    global_std = np.std(tofc_data)
    if global_std == 0:
        global_std = 1.0
    return global_mean, global_std

def normalize_input(data, global_mean, global_std):
    """Normalize input data using global statistics"""
    return (data - global_mean) / (global_std + 1e-8)

