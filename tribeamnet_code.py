def process_file(file_path, patch_rows, fs_constant):
    with h5py.File(file_path, "r") as f:
        # Load raw data from 'raw_data'
        inp = f['raw_data'][:]

        # Load all three ground truth beamformed data and their scales
        gt_das_int16 = f['bf_data_DAS_real'][:]
        scale_das = f.attrs['scale_DAS_real']
        gt_fdmas_int16 = f['bf_data_FDMAS_real'][:]
        scale_fdmas = f.attrs['scale_FDMAS_real']
        gt_capon_int16 = f['bf_data_Capon_real'][:]
        scale_capon = f.attrs['scale_Capon_real']

        # Scale back to original float values
        gt_das_full = gt_das_int16 * scale_das
        gt_fdmas_full = gt_fdmas_int16 * scale_fdmas
        gt_capon_full = gt_capon_int16 * scale_capon

    idata = np.transpose(inp[0], (1, 0))

    # Perform Time-Of-Flight Correction (TOFC) mapping
    tofc = time_to_space_mapping(idata, fs_constant)

    # CRITICAL: Compute global statistics for consistent normalization
    global_mean, global_std = compute_global_stats(tofc)
    print(f"Global stats - Mean: {global_mean:.6f}, Std: {global_std:.6f}")

    # Determine num_patches dynamically
    total_depth = tofc.shape[0]
    num_patches = (total_depth + patch_rows - 1) // patch_rows

    # Lists to store patches
    pred_full_das = []
    pred_full_fdmas = []
    pred_full_capon = []

    # Lists to store ground truth patches
    gt_full_das_patches = []
    gt_full_fdmas_patches = []
    gt_full_capon_patches = []

    for i in range(num_patches):
        start_row = i * patch_rows
        end_row = min((i + 1) * patch_rows, total_depth)
        if start_row >= total_depth:
            break

        current_patch_rows = end_row - start_row

        input_patch_raw = tofc[start_row:end_row]

        # Extract corresponding ground truth patches
        gt_das_patch_raw = gt_das_full[start_row:end_row]
        gt_fdmas_patch_raw = gt_fdmas_full[start_row:end_row]
        gt_capon_patch_raw = gt_capon_full[start_row:end_row]

        # Pad if the last patch is smaller than patch_rows
        if current_patch_rows < patch_rows:
            padding_rows = patch_rows - current_patch_rows
            input_patch_raw = np.pad(input_patch_raw, ((0, padding_rows), (0, 0), (0, 0)),
                                   mode='constant', constant_values=0)
            gt_das_patch_raw = np.pad(gt_das_patch_raw, ((0, padding_rows), (0, 0)),
                                    mode='constant', constant_values=0)
            gt_fdmas_patch_raw = np.pad(gt_fdmas_patch_raw, ((0, padding_rows), (0, 0)),
                                      mode='constant', constant_values=0)
            gt_capon_patch_raw = np.pad(gt_capon_patch_raw, ((0, padding_rows), (0, 0)),
                                      mode='constant', constant_values=0)

        # Input patch for model: [C, H, W] -> [128, patch_rows, 128]
        input_tensor_np = np.nan_to_num(input_patch_raw.transpose(2, 0, 1))

        # FIXED: Use global normalization instead of per-patch normalization
        input_tensor_np = normalize_input(input_tensor_np, global_mean, global_std)

        # Convert to PyTorch tensor and add batch dimension
        input_tensor = torch.from_numpy(input_tensor_np).unsqueeze(0).float().to(device)

        with torch.no_grad():
            model_outputs = model(input_tensor)

        # Extract predicted patches for each beamformer
        pred_das_patch = model_outputs['das'].squeeze(0).squeeze(0).cpu().numpy()
        pred_fdmas_patch = model_outputs['fdmas'].squeeze(0).squeeze(0).cpu().numpy()
        pred_capon_patch = model_outputs['capon'].squeeze(0).squeeze(0).cpu().numpy()

        # Handle potential extra padding if last patch was smaller
        if current_patch_rows < patch_rows:
            pred_das_patch = pred_das_patch[:current_patch_rows, :]
            pred_fdmas_patch = pred_fdmas_patch[:current_patch_rows, :]
            pred_capon_patch = pred_capon_patch[:current_patch_rows, :]
            gt_das_patch_raw = gt_das_patch_raw[:current_patch_rows, :]
            gt_fdmas_patch_raw = gt_fdmas_patch_raw[:current_patch_rows, :]
            gt_capon_patch_raw = gt_capon_patch_raw[:current_patch_rows, :]

        # Append the processed (predicted and GT) patches to their respective lists
        pred_full_das.append(pred_das_patch)
        pred_full_fdmas.append(pred_fdmas_patch)
        pred_full_capon.append(pred_capon_patch)

        gt_full_das_patches.append(gt_das_patch_raw)
        gt_full_fdmas_patches.append(gt_fdmas_patch_raw)
        gt_full_capon_patches.append(gt_capon_patch_raw)

    # Stack all patches vertically to form the full images
    pred_full_das = np.vstack(pred_full_das)
    pred_full_fdmas = np.vstack(pred_full_fdmas)
    pred_full_capon = np.vstack(pred_full_capon)

    gt_full_das = np.vstack(gt_full_das_patches)
    gt_full_fdmas = np.vstack(gt_full_fdmas_patches)
    gt_full_capon = np.vstack(gt_full_capon_patches)

    return {
        'pred_das': pred_full_das,
        'pred_fdmas': pred_full_fdmas,
        'pred_capon': pred_full_capon,
        'gt_das': gt_full_das.T,
        'gt_fdmas': gt_full_fdmas.T,
        'gt_capon': gt_full_capon.T
    }


