def hilbert_log_compress(img, epsilon=1e-6):
    """
    Applies Hilbert transform to get envelope, then log compresses the image.
    Normalizes the image so its maximum value is 0 dB.
    Args:
        img (np.ndarray): The 2D beamformed image data.
        epsilon (float): Small value to prevent log(0).
    Returns:
        np.ndarray: The log-compressed image.
    """
    # Ensure img is 2D (depth, lateral)
    if img.ndim == 3 and img.shape[0] == 1:
        img = img.squeeze(0)

    # Apply Hilbert transform along the depth axis (axis=0)
    analytic = HilbertTransform(axis=0)(img)
    envelope = torch.abs(analytic)
    envelope_max = torch.max(envelope)

    # Handle potential issues with log(0) by clipping
    if envelope_max > 1e-10:
        # Log compression
        envelope_log = 20 * torch.log10(torch.clamp(envelope / envelope_max, min=1e-8))

        # Normalize
        envelope_min, envelope_max = torch.min(envelope_log), torch.max(envelope_log)
        if envelope_max > envelope_min:
            log_img_norm = (envelope_log - envelope_min) / (envelope_max - envelope_min)
        else:
            log_img_norm = torch.zeros_like(envelope_log)
    else:
        log_img_norm = torch.zeros_like(envelope)

    # Normalize the maximum value to 0 dB
    log_img_norm -= log_img_norm.max()
    return log_img_norm

def time_to_space_mapping(rf_data: np.ndarray, fs: int) -> np.ndarray:
    """
    Performs Time-Of-Flight Correction (TOFC) mapping.
    Args:
        rf_data (np.ndarray): Raw RF data, expected shape [time_samples, channels].
        fs (int): Sampling frequency.
    Returns:
        np.ndarray: TOFC-mapped data, shape [depth_samples, lateral_lines, channels].
    """
    c = 1540  # Speed of sound in m/s
    probe_geometry = np.linspace(-0.019105, 0.019105, 128) # Assuming 128 elements
    timeVector = np.arange(rf_data.shape[0]) / fs # Time axis for RF data

    # Lateral axis in space (assuming lateral_lines = number of channels for now)
    x_axis = np.linspace(probe_geometry[0], probe_geometry[-1], rf_data.shape[1])
    z_axis = 0.5 * c * timeVector # Depth axis in space

    x_grid, z_grid = np.meshgrid(x_axis, z_axis)
    pixels = z_grid.shape[0] * z_grid.shape[1]

    tofc_data = np.zeros((pixels, rf_data.shape[1]), dtype=np.float32)

    for nrx in range(rf_data.shape[1]): # Iterate over each receiver element (channel)
        # Calculate receive delay from each pixel to each receiver element
        receive_delay = np.sqrt((probe_geometry[nrx] - x_grid.ravel()) ** 2 + z_grid.ravel() ** 2)
        # Total two-way delay
        total_delay = (z_grid.ravel() / c) + (receive_delay / c)

        # Interpolate RF data at these total_delay times
        interp_func = interp1d(timeVector, rf_data[:, nrx], kind='cubic', fill_value=0, bounds_error=False)
        tofc_data[:, nrx] = interp_func(total_delay)

    # Reshape TOFC back to [depth_samples, lateral_lines, channels]
    result = tofc_data.reshape(z_grid.shape[0], z_grid.shape[1], rf_data.shape[1])
    return np.nan_to_num(result, nan=0.0, posinf=1.0, neginf=-1.0)

